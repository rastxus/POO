package ej7;

public class Punto {
	@Override
	public String toString() {
		return "Punto [x=" + x + ", y=" + y + "]";
	}
	private double x;
	private double y;
	
	/**
	 * @return the x
	 */
	public double getX() {
		return x;
	}
	
	/**
	 * @param x the x to set
	 */
	public void setX(double x) {
		this.x = x;
	}
	/**
	 * @return the y
	 */
	public double getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(double y) {
		this.y = y;
	}

	
	
	
}
